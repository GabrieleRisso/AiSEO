// Get icon component by source id
export function getAISourceIcon(source: AISource): React.FC<IconProps> {
  switch (source) {
    case 'all':
      return AllModelsIcon;
    case 'ai-overview':
      return AIOverviewIcon;
    case 'chatgpt':
      return ChatGPTIcon;
    case 'claude':
      return ClaudeIcon;
    case 'gemini':
      return GeminiIcon;
    case 'perplexity':
      return PerplexityIcon;
    default:
      return AllModelsIcon;
  }
}

// Render icon for a source
export function AISourceIconRenderer({ source, size = 20, className = '' }: { source: AISource; size?: number; className?: string }) {
  const IconComponent = getAISourceIcon(source);
  return <IconComponent size={size} className={className} />;
}
